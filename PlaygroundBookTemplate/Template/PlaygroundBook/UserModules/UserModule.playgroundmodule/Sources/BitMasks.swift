
///bitmasks for Physics Collisions
struct BitMasks {
    ///bitmasks for bCell collisions
    static let bCell: UInt32 = 1 << 1
    ///bitmasks for Virus collisions
    static let virus: UInt32 = 1 << 2
    ///bitmasks for redBloodCell collisions
    static let redBloodCell: UInt32 = 1 << 3
    ///bitmasks for Antibody collisions
    static let antibody: UInt32 = 1 << 4
    ///bitmasks for BlankVirus collisions
    static let blankVirus: UInt32 = 1 << 5
    ///bitmasks for InfectedCell collisions
    static let infectedCell: UInt32 = 1 << 6
}
